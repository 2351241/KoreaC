// 기계어 : 컴퓨터가 사용하는 언어, 0과 1로 이루어져 있다.
// 컴파일러 : Visual Studio, 사람의 언어(프로그래밍 언어)를 기계어로 바꾸어 주는 도구

// 컴파일러가 프로그래밍 언어를 기계어로 바꾸기 위해 규칙에 맞게끔 작성(C언어 문법)

